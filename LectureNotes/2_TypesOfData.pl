# Types of Data
my $age = 26;
print "My age is $age\n";
print "My age is ".$age."\n";
$age = "26";
$age = $age + 4;
print "My age is ".$age."\n";

print "\tThis is double quoted string.\n";
print '\tThis is single quoted string.\n';
print "\n";

# Data Type Conversion
my $float = 35.7;
my $int = 3;
my $string = '56';

my $a = $float + $int;
print "$float + $int = ".$a."\n";
my $b = $float + $string;
print "$float + $string = ".$b."\n";